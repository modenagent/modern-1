			<h2 class="underline title-bold">Property Vesting</h2>
			<p>How to take ownership of your property
</p>
		</header>

		<p class="page-tagline">Real property is among the most valuable of assets, the question of how parties take ownership of their property is of great importance. The form of ownership taken—the vesting of title—will determine who may sign various documents involving the property and future rights of the parties to the transaction. These rights involve such matters as: real property taxes, income taxes, inheritance and gift taxes, transferability of title and exposure to creditor's claims. Also, how title is vested can have significant probate implications in the event of death.
</p>

		
		<ol class="vesting">
			<p class="vesting-title">Sole Ownership</p>
			<li>1. A Single Man or Woman, an Unmarried Man or Woman or a Widow or Widower: A man or woman who is not legally married or in a domestic partnership.</li>
			<li>2. A Married Man or Woman as His or Her Sole and Separate Property: A married man or woman who wishes to acquire title in his or her name alone.</li>
			<li>3. A Domestic Partner as His or Her Sole and Separate Property: A domestic partner who wishes to acquire title in his or her name alone.</li>
		</ol>



		<ol class="vesting">
			<p class="vesting-title">Co-Ownership</p>
			<li>1. Community Property: A form of vesting title to property owned together by married persons or by domestic partners.</li>
<li>2. Community Property with Right of Survivorship: A form of vesting title to property owned together by spouses or by domestic partners. This form of holding title shares many of the characteristics of community property but adds the benefit of the right of survivorship similar to title held in joint tenancy.</li>
<li>3. Joint Tenancy: A form of vesting title to property owned by two or more persons, who may or may not be married or domestic partners, in equal interests, subject to the right of survivorship in the surviving joint tenant(s).</li>
<li>4. Tenancy in Common: A form of vesting title to property owned by any two or more individuals in undivided fractional interests. These fractional interests may be unequal in quantity or duration and may arise at different times.</li>
		</ol>



		<ol class="vesting">
			<p class="vesting-title">Other Ways Of Vesting</p>
			<li>1. Corporation</li>
			<li>2. Partnership</li>
			<li>3. Trustees of a Trust</li>
			<li>4. Limited Liability Companies ( LLC )</li>
			<li>*In cases of corporate, partnership, LLC or trust ownership - required documents may include corporate articles and bylaws, partnership agreements, LLC operating agreements and trust agreements and/or certificates.</li>
		</ol>

		

