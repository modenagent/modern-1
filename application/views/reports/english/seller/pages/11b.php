			<h2 class="underline title-bold"> A Promise to my Clients </h2>
			<p> My duties to you </p>
		</header>

		<p class="page-tagline">As your real estate agent, in addition to any duties or commitments set forth in our listing agreement, my fiduciary duties to you include but are not limited to:
		</p>

        <ol class="vesting">
                <h3 class="vesting-title">Loyalty </h3>
                <li>
                Loyalty is my first and foremost duty to you.  This means that I must act with your best interest in mind, to the exclusion of all other interests including my own.  </li>

        </ol>



        <ol class="vesting">
                <h3 class="vesting-title"> Confidentiality </h3>
                <li>

        As your agent I am obligated to safeguard your confidence and secrets. I therefore, must keep confidential any information that might weaken your bargaining position if it were revealed.

        </li>

        </ol>



        <ol class="vesting">
                <h3 class="vesting-title">Disclosure</h3>
                <li>
                As your agent, I am responsible to disclose to you, all relevant and material information that I know might affect your ability to obtain the highest price and best terms in the sale of your property.
        </li>

        </ol>


		 <ol class="vesting">
                <h3 class="vesting-title"> Obedience </h3>
                <li>
                As your agent, I am bound to obey promptly and efficiently all lawful instructions that you give me pertaining to the sale of your property.
        </li>

        </ol>


		<ol class="vesting">
                <h3 class="vesting-title"> Reasonable care and diligence </h3>
                <li>
                To assist you in your real estate transaction, the standard of care expected of me, by you, should be that of a competent real estate professional.
        </li>

        </ol>


		 <ol class="vesting">
                <h3 class="vesting-title"> Accounting </h3>
                <li>
                As your realtor, I am bound to safeguard any money, deeds, or other documents you entrust to me, related to your real estate transaction.
        </li>

        </ol>


		

