            <h5>DAY 1:</h5>
            Pre-Escrow – offer has been accepted and purchase agreement h as been signed!
            
            <h5>
                DAY 2 – 5:
            </h5>
            Fund Escrow – Earnest money is deposited. This is a percentage of the
            purchase price held by Escrow.

            <h5>DAY 3 – 20:</h5>
            Disclosures and Inspections – During this period all disclosures should be
            completed and submitted. This is also when inspections are completed.
            Requests for repairs should be submitted based on inspection findings.

            <h5>DAY 17:</h5>
            By day 17 any contingencies must be removed. You also have up until this
            point to cancel the deal if you decide not to move forward. When
            contingencies are removed you are also removing any rights to back out
            without losing your Earnest money.

            <h5>DAY 20+:</h5>
            Securing Funding – At this point your funding should be ready. Any and
            all contingencies should be removed, and we should be wrapping up the
            loan package or submitting any final loan conditions to your underwriter.

            <h5>WITHIN THE FINAL 3 DAYS.</h5>
            Time to Close – Loan funds should be received by escrow and dispersed to
            the appropriate parties. Usually that same day the deed should be
            recorded, which means title has officially been transferred over.

            <h5>LAST DAY:</h5>
            Congrats, you just bought a house!

