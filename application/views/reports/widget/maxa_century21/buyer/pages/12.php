            <h5>
                WHY IS IT IMPORTANT?
            </h5>
            Pre-Approvals set you up for a smooth experience. They let the seller know
            you are a serious buyer. Having a pre-approval in hand might give you that
            upper hand when making offers on a home. It also lets you know how
            much purchasing power you have

            <h5>DISCLOSURES</h5>
            You will need disclosures that confirm the terms of the loan you’ve applie d
            for as well as your Good Faith Estimate and Truth in Lending Statement.

            <h5>APPRAISAL</h5>
            An appraisal provides the estimated market value of the property. This is
            prepared by an independent third party who is licensed and experienced.

            <h5>UNDERWRITING PROCESS:</h5>
            Your package will be submitted to the underwriter who is a key
            decision-maker in your approval. Their job is to ensure you qualify for the
            loan you have applied for

            <h5>APPROVAL</h5>
            After you receive your approval and all conditions are cleared you will
            receive your “Clear to Close” notification as well as your “CD” (Closing
            documents). Escrow will arrange to have you sign them as well as advising
            of any additional funds to close.

            <h5>FUNDING</h5>
            Once all documents have been signed and reviewed, your loan will be
            wired. Once the loan is recorded the transaction is considered closed!
