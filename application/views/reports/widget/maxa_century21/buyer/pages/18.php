            <h5>
                INSPECTIONS
            </h5>
            <p>
                The inspection is an important part of the home
                buying process. Having a third party perform a
                thorough review of the property allows both parties a
                clear understanding of the condition of the property
                before you purchase it. General areas an inspection
                will cover includes defects to the exterior, plumbing,
                electrical, appliances, water heaters, furnace, and any
                other visible features on the property.
            </p>
            <h5>
                TITLE INSURANCE
            </h5>
            <p>
                Title insurance is a guarantee that you are getting
                the property you are about to purchase just as it is
                stated in recording documents. Title gives you the
                rights to the property you purchased and a critical
                point during the escrow process is to ensure that
                the seller had the “title” or legal right to sell it to you.
                Although preliminary title reports can come back
                without any issues, it is possible for a title to come
                back with defects after ordering title. Title
                insurance protects you against those defects so that
                you are not out any money should the title be
                deemed unmarketable or have pending claims.
                Unlike other insurance policies protecting you
                against future situations, title insurance protects
                you against existing issues.
            </p>
