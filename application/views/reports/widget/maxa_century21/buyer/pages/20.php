                <p class="mb-0"><b>CONTINGENCY</b></p>
                <p>
                    If a property is contingent, or the
                    contract contains a contingency, certain
                    events must transpire or the contract
                    can be considered null. A contingency
                    might be that the home must past an
                    appraisal or receive a clean inspection.<br><br>
                    The sale of a home could also be
                    contingent on the buyer selling their
                    home by a specified date. If either the
                    buyer or seller fail to meet the
                    expectations of the contingency, either
                    party can exit the contract.
                </p>
                <p class="mb-0"><b>CONTINGENT VS. PENDING</b></p>
                <p>
                    When a property is contingent, it means
                    the owner has accepted an offer — but
                    certain contractual expectations must
                    be met or the offer will be void. If all
                    contingencies are met, the property
                    changes status to “pending.” While
                    contingent offers are still considered
                    active listings, pending offers are taken
                    off the market and other offers will not
                    be entertained.
                </p>
                <p class="mb-0"><b>DEED</b></p>
                <p>
                    A housing deed is the legal document
                    transferring a title from the seller to the
                    buyer. It must be a written document
                    and is sometimes referred to as the
                    vehicle of the property interest transfer.
                </p>
                <p class="mb-0"><b>DOWN PAYMENT</b></p>
                <p>
                    The down payment is the amount of
                    cash a homebuyer pays at the time of
                    closing. Typical home loans require a
                    20% down payment. Some conforming
                    loans will accept a 5% down payment,
                    and FHA loans will accept a 3.5% down
                    payment.
                </p>
            </div>
            <div class="col-50">
                <p class="mb-0"><b>EARNEST MONEY DEPOSIT</b></p>
                <p>
                    Earnest money is a deposit (usually 1-2%
                    of the home’s total purchase price) made
                    by a homebuyer at the time they enter
                    into a contract with a seller. Earnest
                    money demonstrates the buyer’s interest
                    in the property and is generally deducted
                    from your total down payment and
                    closing costs.
                </p>
                <p class="mb-0"><b>ESCROW</b></p>
                <p>
                    Escrow is part of the homebuying process.
                    It happens when a third party holds
                    something of value during the
                    transaction. Most often, the “value” the
                    third party holds onto is the buyer’s
                    earnest money check. When the
                    transaction is complete (usually at
                    closing), the third party will release those
                    funds to the seller.
                </p>
                <p class="mb-0"><b>FAIR CREDIT REPORTING ACT</b></p>
                <p>
                    The Fair Credit Reporting Act (FCRA) was
                    enacted in 1970 and ensures fairness,
                    accuracy, and privacy of personal
                    information contained in files maintained
                    by credit reporting agencies. The goal of
                    this act is to protect consumers from
                    having misinformation used against them.
                </p>
                <p class="mb-0"><b>FAIR MARKET VALUE</b></p>
                <p>
                    A property’s fair market value is its accurate valuation in a free and open market under the condition that buyers and sellers are knowledgeable about the asset,acting in their best interests, and free of unduepressure to complete the transaction.
                </p>
