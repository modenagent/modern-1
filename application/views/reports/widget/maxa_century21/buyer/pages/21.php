                <p class="mb-0"><b>HOME INSPECTION</b></p>
                <p>
                    A home inspection is carried out by an objective third party to establish the condition of a property during a real estate transaction. An inspector will report on such things as a home’s heating system, the stability of the foundation, and the condition of the roof.The inspection is meant to identify major issues that might affect the value of the home and the stability of your and your lender’s investment and return.
                </p>
                <p class="mb-0"><b>HOMEOWNER’S ASSOCIATION</b></p>
                <p>
                    A homeowner’s association (HOA) is usually found when you purchase a condominium, townhome, or other development property. To purchase the home, you must also join the HOA and pay monthly or yearly HOA fees. <br><br>These fees can cover common area maintenance, repairs, and general up keep. The more amenities your building offers, the higher the HOA fees typically are.
                </p>
                <p class="mb-0"><b>HOMEOWNER’S INSURANCE</b></p>
                <p>
                    When you purchase a home, it's also necessary to purchase home owner’s insurance to cover any losses or damages you might incur, such as natural disaster, theft, or damage. It also protects the homeowner from liability against any accidents in the home or on the property.Insurance payments are usually included in your monthly mortgage payments.
                </p>
            </div>
            <div class="col-50">
                <p class="mb-0"><b>LENDER</b></p>
                <p>
                    In real estate, the lender refers to the individual, financial institution, or private group lending money to a buyer to purchase property with the expectation the loan will be repaid with interest, in agreed upon
                    increments, by a certain date.
                </p>
                <p class="mb-0"><b>MORTGAGE</b></p>
                <p>
                    A mortgage is the agreement between a
                    borrower and a lender giving the lender the
                    right to the borrower’s property if the
                    borrower is unable to make loan payments
                    (with interest) within an agreed upon
                    timeline.
                </p>
                <p class="mb-0"><b>PENDING</b></p>
                <p>
                    A sales is considered “pending” if all
                    contingencies have been met and the buyer
                    and seller are moving toward closing. At this
                    point, it’s unlikely the sale will fall through,
                    and the buyer or seller risk losing the earnest
                    money if they walk out on the deal at this
                    point.
                </p>
                <p class="mb-0"><b>PER DIEM</b></p>
                <p>
                    Perdiem or “perday” fees are charged if a loan isn’t approved by the date the loan was scheduled to be completed. These charges are payable to the lender during closig.
                </p>
