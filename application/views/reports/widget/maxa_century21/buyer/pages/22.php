            <div class="col-50">
                <p class="mb-0"><b>PRE-APPROVAL</b></p>
                <p>
                    Before submitting an offer on a home (or even engaging with a real estate agent) you’ll likely be required to get pre-approved. This means a lender has checked your credit, verified your information, and approved you for upto a specific loan amount for a period of upto 90 days.
                </p>
                <p class="mb-0"><b>PRINCIPAL</b></p>
                <p>
                    The principal of a loan is the amount of money owed on that loan. As you make monthly mortgage payments, your principal — in theory — goes down.The amount of interest you pay on amonthly loan will affect how much of your monthly mortgage payment goes to paying down the principal.A high interest rate means you’ll pay less on the principal, meaning you’ll pay more on your loan over time.
                </p>
                <p class="mb-0"><b>PURCHASE AGREEMENT</b></p>
                <p>
                    A purchase agreement demonstrates a buyer’s intent to purchase a piece of property and aseller’s intent to sell that property.
                    The document outlines the terms and conditions of a sale and holds each party legally accountable to meeting their agreements.
                </p>
            </div>
            <div class="col-50">
                <p class="mb-0"><b>TITLE</b></p>
                <p>
                    A home’s title represents the rights to the property.
                    Those rights are transferred from the seller to the buyer during a real estate transaction and give the buyer legal rights to the property upon closing.
                </p>
                <p class="mb-0"><b>TRANSFER OF OWNERSHIP</b></p>
                <p>
                    In real estate, transfer of ownership refers to transfer of a property’s deed and title from the seller to the buyer at closing.
                </p>
                <p class="mb-0"><b>UNDER CONTRACT</b></p>
                <p>
                    A home is “under contract” when a seller has accepted an offer from a buyer but the transaction has not yet closed.
                </p>
            </div>
