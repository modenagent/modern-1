            <h4 class="table_title mt-50">Billing: When And How</h4>
            <p class="small_text">
                "When" is not easy to predict. You could be billed
                in as few as three weeks, or it could take over six
                months. "When" also depends on the individual
                county and the workload of the County Assessor,
                the County Controller/Auditor and the County Tax
                Collector. The assessor will appraise your property
                and advise you of the new supplemental assessment
                amount. The County will then calculate the amount
                of the supplemental tax and the tax collector will
                mail you a supplemental tax bill.
            </p>
