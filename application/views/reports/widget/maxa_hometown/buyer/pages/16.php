            <h4 class="table_title">Co-Ownership</h4>
            <ol>
                <li>
                    Community Property: A form of vesting title to
                    property owned together by married persons or
                    by domestic partners.
                </li>
                <li>
                    Community Property with Right of Survivorship:
                    A form of vesting title to property owned
                    together by spouses or by domestic partners.
                    This form of holding title shares many of the
                    characteristics of community property but adds
                    the benefi t of the right of survivorship similar to
                    title held in joint tenancy.
                </li>
                <li>
                    Joint Tenancy: A form of vesting title to property
                    owned by two or more persons, who may or may
                    not be married or domestic partners, in equal
                    interests, subject to the right of survivorship in
                    the surviving joint tenant(s).
                </li>
                <li>
                    Tenancy in Common: A form of vesting title to
                    property owned by any two or more individuals
                    in undivided fractional interests. These fractional
                    interests may be unequal in quantity or duration
                    and may arise at diff erent times.
                </li>
            </ol>
